
#include <LSkybox.h>

namespace engine
{

    LSkybox::LSkybox( const std::string& skyboxResId )
    {
        float32 _vertices[] =
        {
            -1.0f,  1.0f, -1.0f,
            -1.0f, -1.0f, -1.0f,
             1.0f, -1.0f, -1.0f,
             1.0f, -1.0f, -1.0f,
             1.0f,  1.0f, -1.0f,
            -1.0f,  1.0f, -1.0f,

            -1.0f, -1.0f,  1.0f,
            -1.0f, -1.0f, -1.0f,
            -1.0f,  1.0f, -1.0f,
            -1.0f,  1.0f, -1.0f,
            -1.0f,  1.0f,  1.0f,
            -1.0f, -1.0f,  1.0f,

             1.0f, -1.0f, -1.0f,
             1.0f, -1.0f,  1.0f,
             1.0f,  1.0f,  1.0f,
             1.0f,  1.0f,  1.0f,
             1.0f,  1.0f, -1.0f,
             1.0f, -1.0f, -1.0f,

            -1.0f, -1.0f,  1.0f,
            -1.0f,  1.0f,  1.0f,
             1.0f,  1.0f,  1.0f,
             1.0f,  1.0f,  1.0f,
             1.0f, -1.0f,  1.0f,
            -1.0f, -1.0f,  1.0f,

            -1.0f,  1.0f, -1.0f,
             1.0f,  1.0f, -1.0f,
             1.0f,  1.0f,  1.0f,
             1.0f,  1.0f,  1.0f,
            -1.0f,  1.0f,  1.0f,
            -1.0f,  1.0f, -1.0f,

            -1.0f, -1.0f, -1.0f,
            -1.0f, -1.0f,  1.0f,
             1.0f, -1.0f, -1.0f,
             1.0f, -1.0f, -1.0f,
            -1.0f, -1.0f,  1.0f,
             1.0f, -1.0f,  1.0f
        };

        auto _vBuffer = new CVertexBuffer( { { "position", eElementType::Float3, false } },
                                           eBufferUsage::STATIC,
                                           sizeof( _vertices ),
                                           _vertices );

        m_vertexArray = new CVertexArray();
        m_vertexArray->addVertexBuffer( _vBuffer );

        m_textureCube = CTextureManager::GetCachedTextureCube( skyboxResId );
    }

    LSkybox::~LSkybox()
    {
        if ( m_vertexArray )
            delete m_vertexArray;

        m_vertexArray = NULL;
    }

    void LSkybox::render()
    {
        m_textureCube->bind();
        m_vertexArray->bind();

        glDrawArrays( GL_TRIANGLES, 0, 36 );

        m_vertexArray->unbind();
        m_textureCube->unbind();
    }

}